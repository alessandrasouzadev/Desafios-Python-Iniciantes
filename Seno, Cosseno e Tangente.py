import math
ang = int(input('Qual o ângulo do círculo referente? '))
con = math.cos(math.radians(ang))
sen = math.sin(math.radians(ang))
tan = math.tan(math.radians(ang))
print('O ângulo referente tem o valor do COSSENO de {:.2f}\ndo SENO {:.2f} e\ndo TANGENTE {:.2f}' .format(con,sen,tan))

